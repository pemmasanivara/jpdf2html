package pdf2html.util;

public class PDF2HTMLException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8898436568948169084L;

	public PDF2HTMLException() {
		super();
	}

	public PDF2HTMLException(Throwable throwable) {
		super(throwable);
	}

	public PDF2HTMLException(String message) {
		super(message);
	}

	public PDF2HTMLException(String message, Throwable throwable) {
		super(message, throwable);
	}

}
